'''
연산자
    특정한 작업을 수행하기 위해서 사용하는 기호를 의미
    
1. 산술연산자
    숫자 계산을 할 떄 사용하는 연산자
    종류: +, -, *, **(고듭제곱), /, //(나눴을때 몫), %(나눈 나머지)
'''
a = 7
b = 2
#덧셈
print(f'{a} + {b} = {a+b}')
#뺄셈
print(f'{a} - {b} = {a-b}')
#곱셈
print(f'{a} * {b} = {a*b}')
#나누기
print(f'{a} / {b} = {a/b}')
#몫
print(f'{a} // {b} = {a//b}')
#나머지
print(f'{a} % {b} = {a%b}')